#include <kcontrol.h>


int main(int argc, char **argv)
{
  KControlApplication app(argc, argv, "kcmsample");
  app.setTitle(klocale->translate("KDE Control Application Sample"));

  if (app.runGUI())	
    return app.exec();
  else
    return 0;
}
