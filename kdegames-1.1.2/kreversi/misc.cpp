#ifdef DEBUGGING
#include <stdio.h>
#include <stdarg.h>

void DBG(char *msg, ...) {
  static long msgnr = 0;
  va_list ap;
      
  va_start(ap, msg);
  fprintf(stderr, "(DBG #=%ld) : ", msgnr++);
  vfprintf(stderr, msg, ap);
  fprintf(stderr, "\n");
  va_end(ap);
}
#else
inline int DBG(char *msg, ...) { return 0;}
#endif

